let days = 'f';

switch (days) {
    case 's':
        console.log('sunday');
        break;
    case 'm':
        console.log('monday');
        break;
    case 't':
        console.log('tuesday');
        break;
    case 'w':
        console.log('wednesday');
        break;
    case 'th':
        console.log('thursday');
        break;
    case 'f':
        console.log('friday');
        break;
    case 'sat':
        console.log('saturday');
        break;
    default:
        console.log('invalid input')
        break;
}

